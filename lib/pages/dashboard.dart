class DashBoard extends StatefulWidget {
  @override
  _DashBoardState createState() => _DashBoardState();
}

class _DashBoardState extends State<DashBoard> {
  final CategoriesScroller categoriesScroller = CategoriesScroller();
  ScrollController controller = ScrollController();
  bool closeTopContainer = false;
  double topContainer = 0;

  List<Widget> itemsData = [];
  void getPostsData() {
    List<dynamic> responseList = BOOK_DATA;
    List<Widget> listItems = [];
    responseList.forEach((post) {
      listItems.add(Container(
          height: 150,
          margin: const EdgeInsets.symmetric(horizontal: 20, vertical: 10),
          decoration: BoxDecoration(


              borderRadius: BorderRadius.all(
                  Radius.circular(20.0)), color: Colors.white, boxShadow:
          [
            BoxShadow(color: Colors.black.withAlpha(100), blurRadius: 10.0),
          ]

          ),

          child: Padding(
            padding: const EdgeInsets.symmetric(horizontal: 20.0, vertical: 10),
            child: Row(
              mainAxisAlignment: MainAxisAlignment.spaceBetween,
              children: <Widget>[
                Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: <Widget>[
                    Text(
                      post["name"],
                      style: const TextStyle(fontSize: 28, fontWeight: FontWeight.bold),
                    ),
                    Text(
                      post["publication"],
                      style: const TextStyle(fontSize: 17, color: Colors.grey),
                    ),
                    SizedBox(
                      height: 10,
                    ),
                    Text(
                      "\Rs ${post["price"]}",
                      style: const TextStyle(fontSize: 25, color: Colors.black, fontWeight: FontWeight.bold),
                    )
                  ],
                ),

                Image.asset(
                  "assets/images/${post["image"]}",
                  height: double.infinity,
                )

              ],
            ),

          )));
    });
    setState(() {
      itemsData = listItems;
    });
  }

  @override
  void initState() {
    super.initState();
    getPostsData();
    controller.addListener(() {

      double value = controller.offset/119;

      setState(() {
        topContainer = value;
        closeTopContainer = controller.offset > 50;
      });
    });
  }

  @override
  Widget build(BuildContext context) {
    final Size size = MediaQuery.of(context).size;
    final double categoryHeight = size.height*0.30;
    var color1 = Color.fromARGB(255, 124, 57, 167);
    return Scaffold(
      drawer: NavDrawer(),
      appBar: AppBar(
        title: Text('Padhne Sathi'),
        titleTextStyle: TextStyle(color: Colors.blueGrey),
        backgroundColor: color1,
        actions: [
          IconButton(icon:const Icon(Icons.search),
            onPressed: (){
            },
          ),

          IconButton(icon:const Icon(Icons.info),
            onPressed: (){
            },
          ),
        ],
      ),

      body: Container(
        //decoration: BoxDecoration(
        //gradient: LinearGradient(
        //colors: [
        //Color.fromRGBO(82, 13, 132, 0.9019607843137255),
        //Color.fromRGBO(101, 39, 171, 1.0),
        //],
        //begin: FractionalOffset.bottomCenter,
        //end: FractionalOffset.topCenter,
        //),
        //),
        height: size.height,
        child: Column(
          children: <Widget>[
            const SizedBox(
              height: 10,
            ),
            AnimatedOpacity(
              duration: const Duration(milliseconds: 200),
              opacity: closeTopContainer?0:1,
              child: AnimatedContainer(
                  duration: const Duration(milliseconds: 200),
                  width: size.width,
                  alignment: Alignment.topCenter,
                  height: closeTopContainer?0:categoryHeight,
                  child: categoriesScroller),
            ),
            Expanded(
                child: ListView.builder(
                    controller: controller,
                    itemCount: itemsData.length,
                    physics: BouncingScrollPhysics(),
                    itemBuilder: (context, index) {
                      double scale = 1.0;
                      if (topContainer > 0.5) {
                        scale = index + 0.5 - topContainer;
                        if (scale < 0) {
                          scale = 0;
                        } else if (scale > 1) {
                          scale = 1;
                        }
                      }
                      return Opacity(
                        opacity: scale,
                        child: Transform(
                          transform:  Matrix4.identity()..scale(scale,scale),
                          alignment: Alignment.bottomCenter,
                          child: Align(
                              heightFactor: 0.7,
                              alignment: Alignment.topCenter,
                              child: itemsData[index]),
                        ),
                      );
                    })),
          ],

        ),
      ),

    );
  }
}





